package basic_java;

public class variabesbasic {
    public static void main(String[] args) {
        int x; //declaration
        x=10; //initialization
        System.out.println(x);
        int a = 5;
        System.out.println(a);
        int b = 6;
        System.out.println(b);
        a = b;
        System.out.println(a);
        x = x+2;
        System.out.println(x);
        x = x-2;
        System.out.println(x);
        x = x*2;
        System.out.println(x);
        x = x/2;
        System.out.println(x);

    }
}
