package basic_java;

public class numberprint {
    public static void main(String[] args) {

        // Prints the integer value 87
        System.out.println(87);

        // Prints the text "x+10" exactly as it is because it is inside double quotes
        System.out.println("x+10");

        // Adds 10 and 87, then prints the result (97)
        System.out.println(10 + 87);

    }
}