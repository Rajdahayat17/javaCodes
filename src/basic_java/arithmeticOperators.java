package basic_java;

public class arithmeticOperators {
    public static void main(String[] args){
        int a = 7 ,b = 4;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        double x = 34 ,y= 16;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);



    }

}
