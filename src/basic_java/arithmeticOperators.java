package basic_java;

public class arithmeticOperators {
    public static void main(String[] args){

        // Declare and initialize two integer variables
        int a = 7, b = 4;

        // Addition of two integers
        System.out.println(a + b);

        // Subtraction of two integers
        System.out.println(a - b);

        // Multiplication of two integers
        System.out.println(a * b);

        // Integer division
        // Since both operands are integers, the result is also an integer (fractional part is discarded)
        System.out.println(a / b);



        // Declare and initialize two double variables
        double x = 34, y = 16;

        // Addition of two double values
        System.out.println(x + y);

        // Subtraction of two double values
        System.out.println(x - y);

        // Multiplication of two double values
        System.out.println(x * y);

        // Floating-point division
        // Since operands are doubles, the result includes decimal precision
        System.out.println(x / y);
    }
}